package logical_problam;

public class example {

	public static void main(String[] args)
	{
		for(int a=1; a<=20; a++) 
		{
			System.out.print(a+" ");
			
		}
		System.out.println("\n+++++++++++++++++++++++++++++++++++++++");
		
		for(int s=20; s>=1; s-- ) 
		{
			System.out.print(s+" ");
		}
	}

}
